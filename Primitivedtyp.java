package corejava;

public class primitivedtyp {
	public static void main(String[]args) {
		int x = 5;
		int y = 4;
		System.out.println("x is : " + x);
		System.out.println("y is : " + y);
		byte b = 10;
		System.out.println("b is : "+ b);
		byte b2 = (byte)129;
		System.out.println("b2 is : "+ b2);
		int i = b;
		System.out.println("i is : "+i);
	}
}
